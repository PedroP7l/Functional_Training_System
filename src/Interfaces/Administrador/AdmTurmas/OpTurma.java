    private void EditarCadTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarCadTurmaActionPerformed
        // Botão Editar

        try {            
            Turma tempTurma2 = new Turma();
            
            if(tempTurma == null)
                throw new Exception("Selecione uma turma na tabela");
                
            tempTurma2.setInstrutor(new InstrutorDAO().getInstrutor(((Instrutor)instrCadTurma.getSelectedItem()).getCPF()));
            tempTurma2.setQuantidade(Integer.parseInt(qtdMaxTurmaCtrl.getValue().toString()));
            tempTurma2.setHorario((String) segCadTurma.getSelectedItem() + "|"
                    + (String) terCadTurma.getSelectedItem() + "|"
                    + (String) quaCadTurma.getSelectedItem() + "|"
                    + (String) quinCadTurma.getSelectedItem() + "|"
                    + (String) sexCadTurma.getSelectedItem());
            

            for(Turma t : turmaBanco.getTurmasInstrutor(tempTurma2.getInstrutor().getCPF())){
                if(t.getId() != tempTurma.getId()){
                    if( (!(t.getHorario().split("\\|")[0].equals("Nenhum")) && t.getHorario().split("\\|")[0].equals(tempTurma2.getHorario().split("\\|")[0])) ||
                        (!(t.getHorario().split("\\|")[1].equals("Nenhum")) && t.getHorario().split("\\|")[1].equals(tempTurma2.getHorario().split("\\|")[1])) ||
                        (!(t.getHorario().split("\\|")[2].equals("Nenhum")) && t.getHorario().split("\\|")[2].equals(tempTurma2.getHorario().split("\\|")[2])) ||    
                        (!(t.getHorario().split("\\|")[3].equals("Nenhum")) && t.getHorario().split("\\|")[3].equals(tempTurma2.getHorario().split("\\|")[3])) ||    
                        (!(t.getHorario().split("\\|")[4].equals("Nenhum")) && t.getHorario().split("\\|")[4].equals(tempTurma2.getHorario().split("\\|")[4])))
                        throw new Exception("O Instrutor não pode ser alocado para esses horários");
                }
            }

            tempTurma2.setId((int) TabelaTurma.getValueAt(TabelaTurma.getSelectedRow(), 0));

            EditTurmaAlerta ETA = new EditTurmaAlerta(tempTurma, tempTurma2, this);
            ETA.setVisible(true);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_EditarCadTurmaActionPerformed

