    public void AtualizaTabela() {

        try {
            DefaultTableModel TF = (DefaultTableModel) TabelaTurma.getModel();
            TF.setNumRows(0);
            
            ArrayList<Turma> turmas = turmaBanco.getLista();
            
            for (Turma t : turmas) {                
                TF.addRow(new Object[]{
                    t.getId(),
                    t.getInstrutor().getNome(),
                    t.getAlunos().size(),
                    t.getQuantidade(),});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }

