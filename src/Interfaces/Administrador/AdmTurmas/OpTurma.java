    private void PesquisarTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarTurmaActionPerformed
        try {
            // Botão Pesquisar Turma

            DefaultTableModel TF = (DefaultTableModel) TabelaTurma.getModel();
            TF.setNumRows(0);
            
            int idTurma = Integer.valueOf(pesquisarTurma.getText());
            Turma turma = turmaBanco.getTurma(idTurma);            
            
            if(turma != null){
                if (turma.getId() == Integer.parseInt(pesquisarTurma.getText())) {
                    TF.setNumRows(0);

                    TF.addRow(new Object[]{
                        turma.getId(),
                        turma.getInstrutor().getNome(),
                        turma.getAlunos().size(),
                        turma.getQuantidade(),});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Parâmetro de pesquisa inválido");
        }
    }//GEN-LAST:event_PesquisarTurmaActionPerformed

