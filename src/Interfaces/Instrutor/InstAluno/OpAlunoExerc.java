    public void LimparExercicio(){
        btnAdicionar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        
        nomeExercicio.setText("");
        numeroRepeticao.setValue(0);
        comboEquipamento.setSelectedItem(null);
   
        AtualizaTabelaExercicio();        
    }
    
    public void AtualizaTabelaExercicio(){
        try {
            DefaultTableModel TF = (DefaultTableModel) tableExercicios.getModel();
            TF.setNumRows(0);
            
            for(Exercicio e : new ExercicioDAO().getExercicios(tempAluno.getCPF())){
                
                TF.addRow(new Object[]{
                    e.getNome(),
                    e.getRepeticao(),
                    e.getEquipamento().getNome(),});
                    
            }
        } catch (Exception ex) {
            Logger.getLogger(OpAlunoExerc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void Limpar(){
        ((DefaultTableModel)tableExercicios.getModel()).setNumRows(0);
        ((DefaultTableModel)tableAlunos.getModel()).setNumRows(0);
        
        btnAdicionar.setEnabled(false);
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
        
        nomeAluno.setText("");
        nomeExercicio.setText("");
        numeroRepeticao.setValue(0);
        comboEquipamento.setSelectedItem(null);
        
        restricaoAluno.setText("");
        fotoAluno.setIcon(iconScale(new ImageIcon(getClass().getResource("/Imagens/Perfil.png"))));
        
        jTabbedPane1.setSelectedIndex(0);        
        
    }
    

