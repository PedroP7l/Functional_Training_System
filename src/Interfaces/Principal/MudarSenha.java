    private void SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarActionPerformed
        try {
            if(CampoSenha.getText().equals("123456") || CampoSenha1.getText().equals("123456"))            
                throw new Exception("Digite uma senha válida");
            
          
            if(!CampoSenha.getText().equals(CampoSenha1.getText()))         
                throw new Exception("As senhas são diferentes");
            
            funcionario.setSenha(CampoSenha1.getText());
            
            new FuncionarioDAO().altera(funcionario);
            
            menu.setEnabled(true);
            dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
            
        
    }//GEN-LAST:event_SalvarActionPerformed

