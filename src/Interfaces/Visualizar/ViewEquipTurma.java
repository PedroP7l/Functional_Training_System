    private void formAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_formAncestorAdded
        try {
            DefaultTableModel TF = (DefaultTableModel) tableTurmas.getModel();
            TF.setNumRows(0);
            
            ((DefaultTableModel)tableEquipamentos.getModel()).setNumRows(0);
            
            for(Turma t : new TurmaDAO().getLista()){
                TF.addRow(new Object[]{
                    t.getId(),
                    new Turma_AlunoDAO().qtdAlunos(t.getId()),
                    t.getInstrutor().getNome(),});
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ViewEquipTurma.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_formAncestorAdded

    private void tableTurmasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTurmasMouseClicked
        try {
            if(!(tableTurmas.getSelectedRow() >= 0))
                throw new Exception("Selecione uma turma v√°lida na tabela");
            
            DefaultTableModel TF = (DefaultTableModel) tableEquipamentos.getModel();
            TF.setNumRows(0);
            
            for(String equip : new TurmaDAO().equipamentosTurmas((int) tableTurmas.getValueAt(tableTurmas.getSelectedRow(), 0))){
                TF.addRow(new Object[]{
                    equip,});
            }
            
        } catch (Exception ex) {
            Logger.getLogger(ViewEquipTurma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tableTurmasMouseClicked

